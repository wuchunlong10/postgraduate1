package Entity;

public class Exam {
    
	private String ExamPlace;
	private int ExamNumber;
	private String ExamTeacherList;
	private String ExamSubjectName;
	
	public String getExamPlace() {
		return ExamPlace;
	}
	public void setExamPlace(String examPlace) {
		ExamPlace = examPlace;
	}
	public int getExamNumber() {
		return ExamNumber;
	}
	public void setExamNumber(int examNumber) {
		ExamNumber = examNumber;
	}
	public String getExamTeacherList() {
		return ExamTeacherList;
	}
	public void setExamTeacherList(String examTeacherList) {
		ExamTeacherList = examTeacherList;
	}
	public String getExamSubjectName() {
		return ExamSubjectName;
	}
	public void setExamSubjectName(String examSubjectName) {
		ExamSubjectName = examSubjectName;
	}
	
}
